package com.erick;

public class Vectores {
    public static void main(String[] args) {
        String[] nombres = new String[3];
        nombres[0] = "juan";
        nombres[1] = "pedro";
        nombres[2] = "pablo";

        for (int i = 0; i <= 2; i++) {
            System.out.println(nombres[i]);
        }
    }
}
